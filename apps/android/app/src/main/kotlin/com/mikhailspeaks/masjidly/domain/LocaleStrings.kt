object LocaleStrings {
    fun t(key: String, language: AppLanguage): String =
        tables[language]?.get(key) ?: tables[AppLanguage.ENGLISH]?.get(key) ?: key

    fun format(key: String, language: AppLanguage, vararg args: Any): String {
        var result = t(key, language)
        args.forEach { arg -> result = result.replaceFirst("%s", arg.toString()) }
        return result
    }

